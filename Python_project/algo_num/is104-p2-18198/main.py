def gen_source(n):
    X = []
    Y1 = []
    Y2 = []
    for i in range(n):
        T1,T2,i1,i2=resolve_heat(np.eye(i,i))
        X.append(i)
        Y1.append(i1)
        Y2.append(i2)
    plt.plot(X,Y1,label="sans precond")
    plt.plot(X,Y2,label="avec precond")
    plt.legend()
    plt.xlabel("Taille de la source")
    plt.ylabel("Nombre d'iterations")
    plt.title("Comparaison du nombre d'it√©ration en fonction de la taille de la source")
    plt.show()


